export const parsePageContent = (content, fallback = {}) => {
  if (!content) return fallback;
  if (typeof content === 'object') return { ...fallback, ...content };
  try {
    const parsed = JSON.parse(content);
    return parsed && typeof parsed === 'object' ? { ...fallback, ...parsed } : fallback;
  } catch {
    return { ...fallback, body: content };
  }
};

export const serializePageContent = (content) => JSON.stringify(content, null, 2);
